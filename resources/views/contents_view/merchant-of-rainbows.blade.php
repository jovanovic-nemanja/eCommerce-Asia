									<p>“I see skies of blue and clouds of white/ the bright blessed day, the dark sacred night/ and I think to myself what a wonderful world.”</p>

<p>This is how the protagonist in Louis Armstrong’s iconic song sees the world around. With references to green trees, red roses, blue skies, white clouds, friends shaking hands and babies growing up, the lyrics inspire us to notice the colours of life despite all the pains and sufferings.</p> 

<p>Kazi Ahmed is one of those who look upon life this way, those who know, like the protagonist, “The colours of the rainbow so pretty in the sky …”

Kazi is not just any other Bangladeshi living away from home. He is a rainbow man, glittering in the West but not seen from home yet.</p>

<p>With years of efforts, he came up with the world's first patented markers to make true rainbows without gaps between colours. A rather simple innovation, but his paintbrush has sent kids on a colour-frenzy.</p>

<p>Children can combine the markers in any direction, and then squeeze those to blend colours and paint their masterpiece! No need to buy separate paints, palettes and brushes. No mixing, set-up or mess to clean. It's just like using a pen.</p>

			
<p>With his 50 art products, about 15 million children outside Bangladesh are now making rainbows at their houses and schools, every day.</p>

 

<p>"My products are being sold in over 27 countries," Kazi, back to the country after long 28 years, tells The Daily Star.</p>

<p>He has seven international patents for the RainbowBrush (brand name of his products) and offices in Hong Kong, Canada and the US, with his business netting over $20m revenue a year and still growing.</p>

<p>His products are the number one selling item at the famous Toys R Us in Hong Kong and China. And the RainbowBrush has now only the formidable Disney to cross in the world market.</p>

<p>"I usually don't believe in luck. I believe luck will be on my side only if I continue to work harder," said the 50-year-old, who has homes in Canada and the US.</p>

<p>Business is great and so is his family life. His love for the art revolves round wife Cindy and three children -- Maia, Alexander and Melody. He lives mostly in Florida and gets warm welcome wherever in the world he travels to. Life could not have been any better for Kazi.</p>

<p>It was, however, just the opposite of the life he had left behind decades back. His lips quivered, eyes welled up with tears and voice cracked recalling his past and struggles of his family, especially of his mother. "Sometimes, my poor mother had to collect rice from paddy fields after harvest," he said, wiping off tears.</p>

<p>Eldest son of poor parents, Kazi grew up in extreme hardship. Education appeared a distant dream, with his factory worker father trying hard to make ends meet. But he was born as if to defy the odds. He passed days without meals but stayed focused on rewriting his own fate that went through numerous twists and turns. </p>

<p>As a child he worked. As a student he taught others. As a lodging teacher he earned meals. It was a long struggle, but he could eventually drag himself up to the University of Dhaka. As a student of physics, he dreamed of invention. But, by then, he found it harder to pursue his studies as he needed to support his family as well.</p>

<p>In 1986, he landed himself in Saudi Arabia with a telex operator's job. But fax machine replaced telex soon after. So, he was given the job of a gardener. His quest for innovation came alive again as he had lot of time to spare. </p>

<p>With parts from junkyards of his employer, Kazi went on to invent an oven that uses up to 40 percent less energy in cooking.</p>

<p>The National Research Council of Canada was impressed with Saudi newspaper reports on his innovation and invited him to move over to Canada for demonstration.</p>

<p>Kazi moved to Vancouver in 1989 but became a street artist in the end.</p>

<p>He started what he calls Kazigraphy, a style of calligraphy wherein the lines associated with letters become multi-coloured fish, plant and animal. Like the way moths are drawn to flames, children went for his name-painting art. Everyone wanted to be an artist. He created the scope for them with his markers. And a businessman was born.</p>

<p>Time flew by. Mother died. Father remarried. And he got himself de-linked from his roots.</p>

<p>But the rice field under the blue sky, floating clouds over mango trees, seagulls and the kite he used to fly near his uncle's watermelon field kept coming back to his mind.</p>

<p>He now aims to do something for people like him in his motherland. "Yes, my heart cries out more than often for my country," admits Kazi.</p>

<p>"No doubt there are many youngsters who could be world class leaders, innovators, scientists, economists if opportunity is given."</p>

<p>He wants to initiate a lot of things, from scholarships to IT village. He wants to give the hopeless an opportunity.</p>

<p>He wants to say, “Look at me: Hope is always there.”</p>

			
