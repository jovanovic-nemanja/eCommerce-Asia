      							<h3 class="content-h4-a">Future market of Bangladesh</h3>
      							<p class="portal-content-p">Euro monitor International identifies Bangladesh as one of the 20 Markets of the Future that will offer the most opportunities for consumer goods companies globally. The country is one of the fastest growing and most promising economies in Asia-Pacific; however, market intelligence is extremely limited in this part of the world and the business environment is complicated. Based on in-depth local research, store checks and interviews with key market players and consumers, for the first time Euro monitor International aims to provide extensive assessment of Bangladesh’s major consumer industries.</p>
<p class="portal-content-p">This report offers the world’s most comprehensive market sizes and brand shares for all major consumer markets, complemented by analysis of Bangladesh’s business environment, economic and demographic situation, infrastructure, communications and consumer lifestyles. Data and market analysis will advance your knowledge of the country and its competitive environment, ensuring accurate and focused strategies for your business. With over 50 consumer categories covered, the report will provide a full picture of Bangladeshi consumers, and enable you to pin-point market opportunities.</p>

<h3 class="content-h4-a">Economic growth and diversification to bolster consumption</h3>
<p class="portal-content-p">Bangladesh’s economy performed strongly with average annual real GDP growth of 6% over 2009-2013. The economic outlook remains positive and it is anticipated that real annual GDP growth will stand at 7% on average over 2014-2018. The garment industry is the backbone of Bangladesh’s economy, although the country’s authorities are planning to diversify the economy and encourage investment in the electronics, frozen foods, light engineering and power industries.</p>
<h3 class="content-h4-a">Growing population set to reach 170 million by 2020</h3>
<p class="portal-content-p">
	Bangladesh had a population of 157 million in 2013, and combined with a small territory it represented one of the most densely populated countries in the world. Only around 30% of the population lived in urban areas at the end of the review period, although the expanding garment industry and growing employment opportunities are encouraging more people to live in urban areas. The mean age of the Bangladeshi population stood at 30 years in 2013, and it is anticipated that the population in Bangladesh will reach 170 million by 2020.
</p>
<h3 class="content-h4-a">Growing sales of modern retail</h3>
<p class="portal-content-p">Retail trade is a traditional business in Bangladesh, although modern retail constitutes only around 10% of total sales. However, the situation is changing, and the revenue of modern retailers increased by 15% annually over the review period, twice exceeding the growth rate of the overall retail environment. It is also anticipated that the turnover of modern retailing will increase 4-fold by 2020, reaching US$37 billion</p>
<h3 class="content-h4-a">All consumer categories growing</h3>
<p class="portal-content-p">All consumer categories performed strongly in Bangladesh over 2009-2013. Packaged food was valued at US$9.8 billion in 2013 after registering a CAGR of 20% from 2009. Dairy (CAGR 37%), baby food (CAGR 31%), noodles and pasta (CAGR 28%) and confectionery (CAGR 25%) performed exceptionally well. Overall packaged food sales are anticipated to grow at a CAGR of 17% over 2013-2018, reaching a value of US$21.2 billion in the latter year.</p>
<p class="portal-content-p">Soft drinks market value amounted to US$235 million in 2013 after registering a CAGR of 18% in value terms since 2009. Juice (CAGR 23%), bottled water and sports and energy drinks (19% CAGR each) and carbonates (16% CAGR) were the most dynamic soft drinks categories. It is forecast that soft drinks will see a value CAGR of 15% over 2013-2018.</p>
<p class="portal-content-p">Growing disposable income and an expanding urban middle class also bolstered sales of non-essential items. As a result, categories such as beauty and personal care products (CAGR 15% over 2009-2013), home care (CAGR 12%) and tissue and hygiene (CAGR 20%) also performed strongly.</p>
<h3 class="content-h4-a">Multinational companies capitalize on growing demand for higher-quality products</h3>
<p class="portal-content-p">Domestic producers hold strong positions in nearly all consumer goods categories due to lower prices, making it difficult for multinational companies to compete. As a result foreign companies in Bangladesh are mainly targeting the growing urban middle class population. Additionally, multinationals are capitalizing on Bangladeshi consumers’ perception that foreign brands provide better quality.</p>
<p class="portal-content-p">Multinationals also benefit from growing demand for higher quality processed food. Bangladeshis mostly consume basic staple foods, but with urbanization, rising income and increasingly busy lifestyles, consumer preferences are shifting dramatically. Bangladeshis are increasingly consuming quality processed foods such as sauces, jams, noodles, soft drinks, juices and dairy products.
</p>
<h3 class="content-h4-a">Elimination of infrastructure constraints to improve business environment</h3>
<p class="portal-content-p">
Bangladesh is one of the most attractive nations in the South-east Asia region in which to do business. However, a poor transportation network and inadequate electricity supply remain the main obstacles hindering the business climate in Bangladesh. To solve these issues significant improvements in infrastructure are anticipated in coming years.</p>
<p class="portal-content-p">To improve the transport system the country’s authorities are constructing city bypasses and investing in public transportation. In addition, Bangladesh has a well-developed railway network and one the longest inland waterway systems in the world, which could be considered an alternative to the strained road transport system.</p>
<p class="portal-content-p">Electricity shortages are also expected to be eliminated following the construction of new public and private power plants by 2016. In addition, the Power System Master Plan sets to diversify electricity generation by 2030, with 50% of energy composition attributable to coal, 25% to natural gas, 5% to liquid fuel and the remaining 20% to nuclear fuel and renewable.</p>

</p>
